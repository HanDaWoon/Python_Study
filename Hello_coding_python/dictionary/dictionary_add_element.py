# 딕셔너리 선언

print("# 딕셔너리 선언한다.")
print("dictionary = {}\n")
dict = {}

# 딕셔너리 요소 추가

print("# 딕셔너리 요소 추가한다.")
print('dictionary["name"] = "new name"')
print('dictionary["head"] = "new head"')
print('dictionary["body"] = "new body"')
print('dictionary["element"] = "new name", "new head", "new body"')
dict["name"] = "new name"
dict["head"] = "new head"
dict["body"] = "new body"
dict["element"] = ["new name", "new head", "new body"]

# 딕셔너리 출력

print("\n# 딕셔너리 출력한다.")
print(dict)