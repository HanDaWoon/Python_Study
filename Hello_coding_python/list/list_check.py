# 값이 리스트 내부에 있는 지 확인하기

print("리스트 선언하기")
list_a = [1, 2, 3, "HD", 4, 5]
print("list_a = [1, 2, 3, \"HD\", 4, 5]")
print()

print("1 in list_a :", 1 in list_a, "\n")

print("\"HD\" in list_a :", "HD" in list_a, "\n")

print("0 in list_a :", 0 in list_a, "\n")

print("\"HH\" in list_a :", "HH" in list_a, "\n")

print("0 not in list_a :", 0 not in list_a, "\n")

print("\"HD\" not in list_a :", "HD" not in list_a, "\n")

print("0 not in list_a :", 0 not in list_a, "\n")

print("\"HH\" not in list_a :", "HH" not in list_a)
